#include<iostream>
using namespace std;
int main()
{
	float n1,n2;
	cout<<"enter the first floating-point:";
	cin>>n1;
	cout<<"enter the second floating point:";
	cin>>n2;
	float sum=n1+n2;
	cout<<"sum="<<sum<<endl;
	float difference=n1-n2;
	cout<<"difference="<<difference<<endl;
	float product=n1*n2;
	cout<<"product="<<product<<endl;
	float quotient=n1/n2;
	cout<<"quotient="<<quotient<<endl;
	return 0;
}
